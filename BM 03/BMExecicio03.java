public class BMExecicio03 {
    public static double MetrosParaPes(double metros) {
        double valorpes;
        valorpes = metros * 10.76;
        return valorpes;
    }
    public static double PesParaCentimetros(double valorPes) {
        double centimetros;
        centimetros = valorPes * 929;
        return centimetros;
    }
    public static double MilhaParaAcres(double milhas) {
        double valoracres;
        valoracres = milhas * 640;
        return valoracres;
    }
    public static double AcreParaPes(double acre) {
        double valorpes = acre * 43.56;
        return valorpes;
    }
}